<p style="margin-top:20px">
	<strong>Version 1.5</strong> : Permet de restreindre la visibilité du contenu des articles.</strong>.
	<ul>
		<li>
			Le contenu des articles peux être remplacé par un texte pouvant contenir les balises &lt;div&gt;, &lt;a&gt;, &lt;p&gt;, &lt;br&gt;, &lt;span&gt;, &lt;em&gt;, &lt;b&gt;
		</li>
		<li>
			Dans chaque article, vous pouvez choisir la visibilité entre "par défaut" (suivra la regle de la configuration du plugin), Publique (tout le monde peux lire l'article), Membres: (seul les membres peuvent lire l'article)
		</li>
		<li>
			Le texte de remplacement est affiché dans une div de class=art_content_replaced
		</li>
		<li>
			Possibilité d'ajout de texte avant ou après le chapô d'un article réservé aux membres.
	</ul>
</p>
